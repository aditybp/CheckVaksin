package com.example.checkvaksin.Model;

public class DataModel {
    private int id;
    private String nama_vaksin, keterangan, dosis, jumlah_suntikan, jarak_suntikan, batas_umur, jumlah_vaksin, gambar;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama_vaksin() {
        return nama_vaksin;
    }

    public void setNama_vaksin(String nama_vaksin) {
        this.nama_vaksin = nama_vaksin;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getDosis() {
        return dosis;
    }

    public void setDosis(String dosis) {
        this.dosis = dosis;
    }

    public String getJumlah_suntikan() {
        return jumlah_suntikan;
    }

    public void setJumlah_suntikan(String jumlah_suntikan) {
        this.jumlah_suntikan = jumlah_suntikan;
    }

    public String getJarak_suntikan() {
        return jarak_suntikan;
    }

    public void setJarak_suntikan(String jarak_suntikan) {
        this.jarak_suntikan = jarak_suntikan;
    }

    public String getBatas_umur() {
        return batas_umur;
    }

    public void setBatas_umur(String batas_umur) {
        this.batas_umur = batas_umur;
    }

    public String getJumlah_vaksin() {
        return jumlah_vaksin;
    }

    public void setJumlah_vaksin(String jumlah_vaksin) {
        this.jumlah_vaksin = jumlah_vaksin;
    }

    public String getGambar() {
        return gambar;
    }

    public void setGambar(String gambar) {
        this.gambar = gambar;
    }
}

